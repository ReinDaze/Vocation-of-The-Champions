        </div>
        <ul class="section-content" data-aos="fade-in" data-aos-delay="100">
            <li>Format lomba Fotografi VOC 2024 adalah format online, di mana peserta wajib mengikuti tahapan seleksi dengan penuh tanggung jawab. Keputusan juri tidak dapat diganggu gugat dan akan ada transparansi nilai.</li>
