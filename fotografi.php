            <li>Setiap peserta hanya boleh mengirimkan satu karya fotografi untuk memastikan fokus dan kualitas penilaian.</li>
            <li>Peserta menjamin bahwa karya yang dikirimkan adalah hasil orisinal dan tidak melanggar hak cipta pihak lain. Plagiarisme atau penggunaan karya orang lain akan mengakibatkan diskualifikasi.</li>
            <li>Peserta tidak diperbolehkan mengirimkan karya yang sudah pernah dipublikasikan atau diikutsertakan dalam kompetisi lain. Karya harus bersifat eksklusif untuk lomba ini.</li>
